"""
Evaluation Scripts Module
Computes regression errors (RMSE) and prints side-by-side performance benchmarks.
"""

import numpy as np
from sklearn.metrics import mean_squared_error

def compute_rmse(actual, predicted):
    """
    Calculates Root Mean Squared Error between ground truth and predictions.
    """
    return np.sqrt(mean_squared_error(actual, predicted))

def evaluate_all_pipelines(svd_model, ncf_model, test_df):
    """
    Takes trained models and evaluates them side-by-side against the test data frame split.
    Prints a clean, readable ASCII scorecard summary to the terminal window.
    """
    print("\n. Evaluating models on Test Partition Split...")

    # 1. Generate predictions using Funk SVD Baseline
    # (Surprise requires a loop extracting predictions element by element via .predict())
    svd_preds = [svd_model.predict(row['Customer_Id'], row['Movie_Id']).est for _, row in test_df.iterrows()]
    svd_rmse = compute_rmse(test_df['Rating'], svd_preds)

    # 2. Generate predictions using the Deep NCF Model
    # (Keras maps arrays in batch matrices instantly via model.predict())
    ncf_preds = ncf_model.predict([test_df['user_idx'].values, test_df['movie_idx'].values]).flatten()
    ncf_rmse = compute_rmse(test_df['Rating'], ncf_preds)

    # 3. Print the formatted console scorecard
    print("\n+-------------------------------+---------------+")
    print("|     Algorithmic Structure     |  Target RMSE  |")
    print("+-------------------------------+---------------+")
    print(f"| Funk SVD Baseline             |    {svd_rmse:.4f}     |")
    print(f"| Neural Collaborative (NCF)    |    {ncf_rmse:.4f}     |")
    print("+-------------------------------+---------------+")

    return svd_rmse, ncf_rmse

